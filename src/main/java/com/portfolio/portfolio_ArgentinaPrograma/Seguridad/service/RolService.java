package com.portfolio.portfolio_ArgentinaPrograma.Seguridad.service;

import com.portfolio.portfolio_ArgentinaPrograma.Seguridad.entity.Rol;
import com.portfolio.portfolio_ArgentinaPrograma.Seguridad.enums.RolNombre;
import com.portfolio.portfolio_ArgentinaPrograma.Seguridad.repository.RolRepository;
import java.util.Optional;
import javax.transaction.Transactional;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

/**
 *
 * @author Nahuel
 */
@Service
@Transactional
public class RolService {

    @Autowired
    RolRepository rolRepository;

    public Optional<Rol> getByRolNombre(RolNombre rolNombre){
        return  rolRepository.findByRolNombre(rolNombre);
    }

    public void save(Rol rol){
        rolRepository.save(rol);
    }
}
